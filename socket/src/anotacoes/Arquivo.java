package anotacoes;

public class Arquivo {

	public Arquivo() { }

	String raiz;
	String origem;
	String destino;
	long tamanho;
	
	public Arquivo(String raiz, String origem, String destino, long tamanho) {
		super();
		this.raiz = raiz;
		this.origem = origem;
		this.destino = destino;
		this.tamanho = tamanho;
	}
	
}
